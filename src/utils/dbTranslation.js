/**
 * Translates database terms based on the current active language code.
 * Falls back to the original English text if no translation is found.
 * 
 * @param {string} text The database text to translate (name, source, reason, or alt).
 * @param {string} lng The current active language code (e.g. 'ko', 'ar', 'uz', 'tr', 'en').
 * @returns {string} The localized text.
 */
export function translateDbText(text, lng) {
  if (!text) return '';
  const cleanLng = (lng || 'en').split('-')[0].toLowerCase();
  
  if (cleanLng === 'en' || !DB_TRANSLATIONS[cleanLng]) {
    return text;
  }
  
  const trimmed = text.trim();
  return DB_TRANSLATIONS[cleanLng][trimmed] || text;
}
